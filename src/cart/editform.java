public String getfromdata (String column)
{
    String data="";
  try{  modify="select * from book";
    r= s.executeQuery(modify);
    for(int i=0;i<enteredId.id;i++)
    {     r.next();    }
   data=r.getString(column);
   return data;
  }
  catch(SQLException b)
  {
      JOptionPane.showConfirmDialog(this,b.getMessage());
  }
  return data;
}
