    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
      int uId= userId.id ;
   
     int bookId=enteredId.id;
       String library = UsersManger.getUser().get(uId-1).getLibrary();
     boolean nullChecker = (library == null || library.length() == 0);
     if (!(UsersManger.checkBookStock(bookId))){
     JOptionPane.showMessageDialog(null, "The book you selected is currently out of stock", "Error", JOptionPane.ERROR_MESSAGE);
             }
         else{
     if (nullChecker){
         
     String lib = Integer.toString(bookId);
     UsersManger.writeToLibrary(uId,lib);
     UsersManger.changeCount(bookId);
     JOptionPane.showMessageDialog(this,"Book Successfully added to your cart");

     }
     else if((library.contains(Integer.toString(bookId)))){
     JOptionPane.showMessageDialog(null, "This book already exists in your cart", "Error", JOptionPane.ERROR_MESSAGE);
     } 
     else {
     String lib =UsersManger.getUser().get(uId-1).getLibrary()+','+bookId;
     UsersManger.writeToLibrary(uId,lib);
     UsersManger.changeCount(bookId);
     JOptionPane.showMessageDialog(this,"Book Successfully added to your cart");   
    
     }
     }
        
    }//GEN-LAST:event_jButton1ActionPerformed

